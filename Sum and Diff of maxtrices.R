# Define matrices A and B
A <- matrix(c(2, 0, 1, 3), ncol = 2)
B <- matrix(c(5, 2, 4, -1), ncol = 2)

# Find the sum A + B
sum_result <- A + B

# Find the difference A - B
diff_result <- A - B

# Print the results
cat("Sum (A + B):\n")
print(sum_result)
cat("Difference (A - B):\n")
print(diff_result)
